let gridArtistas = document.getElementById('grid_artistas');
let selectArtistas = document.getElementById('select');

function iniciar() {
    ordenarERenderizar('az');
}

function ordenarERenderizar(criterio) {
    if(!criterio) {
        criterio = selectArtistas.value;
    }

    let listaOrdenada = [...artistas];

    if(criterio == 'az') {
        listaOrdenada.sort((a, b) => a.nome.localeCompare(b.nome));
    } else if (criterio === 'za') {
        listaOrdenada.sort((a, b) => b.nome.localeCompare(a.nome));
    } else if (criterio === 'mais_inscritos') {
        listaOrdenada.sort((a, b) => +b.inscritos - +a.inscritos);
    } else if (criterio === 'menos_inscritos') {
        listaOrdenada.sort((a, b) => +a.inscritos - +b.inscritos);
    }

    renderizarCards(listaOrdenada);
}

function renderizarCards(lista) {
    gridArtistas.innerHTML = lista.map(artistas => `
        <div class="card_artista">
            <img src="${artistas.foto}" alt="${artistas.nome}">
            <span>${artistas.nome}</span>
        </div>
        `).join('');
}

